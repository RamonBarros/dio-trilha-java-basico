public class TiposVariaveis {
    public static void main(String[] args) throws Exception {
        int numero = 5;
        
        numero = 10;
        
        System.out.println(" ");
        System.out.println(numero);
        
        // o "final" é o equivalente ao const em js.
        //por convenção constantes devem ser declaradas em CAIXA ALTA(tudo maisculo)
        final double VALOR_DE_PI = 3.14;
		
		// VALOR_DE_PI=3.15; 

        System.out.println(VALOR_DE_PI);
    }
}
